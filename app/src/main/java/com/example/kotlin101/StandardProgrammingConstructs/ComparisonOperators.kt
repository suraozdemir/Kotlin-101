package com.example.kotlin101.StandardProgrammingConstructs

fun main (){
    var s1 = 60
    var s2 = 50

    var y1 = 70
    var y2 = 80

    println(s1 == s2 )
    println(s1 != s2)
    println(s1 > s2)
    println(s1 >= s2)
    println(s1 < s2)
    println(s1 <= s2)

    println(s1 > s2  || y1 > y2 )
    println( s1 > s2  && y1 > y2 )

}